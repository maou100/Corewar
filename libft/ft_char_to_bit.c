#include "libft.h"

char				*ft_char_to_bit(int nb)
{
	char			*bit;
	int				i;

	i = 7;
	bit = ft_strnew(8);
	while (nb > 0)
	{
		bit[i] = nb % 2 + 48;
		nb /= 2;
		i--;
	}
	while (i >= 0)
		bit[i--] = '0';
	return (bit);
}
